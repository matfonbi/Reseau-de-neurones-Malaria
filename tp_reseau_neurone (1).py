from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score

def calculer_metrics(y_true, y_pred_bin, model_name):
    accuracy = accuracy_score(y_true, y_pred_bin)
    precision = precision_score(y_true, y_pred_bin)
    recall = recall_score(y_true, y_pred_bin)
    f1 = f1_score(y_true, y_pred_bin)

    # Calcul de la sensibilit√© et sp√©cificit√©
    tn, fp, fn, tp = confusion_matrix(y_true, y_pred_bin).ravel()
    sensibilite = tp / (tp + fn)  # Sensibilit√© = Recall
    specificite = tn / (tn + fp)  # Sp√©cificit√©

    # Affichage des m√©triques
    print(f"üìä {model_name} :")
    print(f"   Accuracy : {accuracy:.4f}")
    print(f"   Precision : {precision:.4f}")
    print(f"   Recall / Sensibilit√© : {recall:.4f}")
    print(f"   F1-Score : {f1:.4f}")
    print(f"   Sp√©cificit√© : {specificite:.4f}\n")

# Calcul des m√©triques pour chaque mod√®le
